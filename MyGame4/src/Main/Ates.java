package Main;

public class Ates {
	private int AtesY;
	private int AtesX;
	
	public Ates(int AtesY,int AtesX) {
		this.AtesX=AtesX;
		this.AtesY=AtesY;
	}
	public int getAtesX() {
		return AtesX;
	}
	public int getAtesY() {
		return AtesY;
	}
	public void setAtesY(int deger) {
		AtesY=deger;
	}
	public void setAtesX(int deger) {
		AtesX=deger;
	}

}
